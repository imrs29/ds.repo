class Solution{
    public:
    //Function to count subarrays with sum equal to 0.
    ll findSubarray(vector<ll> arr, int n ) {
        //code here
        unordered_map<ll,ll>mp;
        int sum = 0;
        int c = 0;
        for(int i = 0; i < n; i++){
            sum += arr[i];
            if(sum == 0)
            c++;
            c += mp[sum];
            mp[sum]++;
        }
        return c;
    }
};

